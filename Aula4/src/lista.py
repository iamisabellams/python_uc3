# lista

# definir lista com 3 elementos

lista = [10, "isabella", 30, "flamengo", 50]
print(lista)

# acessar por um indice

print(lista[1])

# alterar por um indice

lista [2] = "isadora"
print(lista)

# adicionar

lista.append('dora')
print(lista)

# adicionar por indice

lista.insert(0, "isaEdora")
print(lista)

# remover por valor

lista.remove(10)
print(lista)

# remover por indice

del lista[2]
print(lista)