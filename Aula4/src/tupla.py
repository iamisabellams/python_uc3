# tupla

# definir uma tupla com 3 elementos

tupla = ("isadora", "isabella", 18, 20)
print(tupla)

# acessar pelo indice

print(tupla[1])

# alterar um valor pelo indice

tupla [2] = "ISAEDORA"
print(tupla)

# adicionar pelo valor

tupla.append('bi')
print(tupla)


# remover pelo valor

tupla.remove(20)
print(tupla)    