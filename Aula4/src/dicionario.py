# dicionario

# definir um dicionario com 3 elementos

dicionario = {
    
    "nome" : "Isabella Medeiros",
    "CPF" : "12345678910",
    "telefone" : "24 9 9999-9999"

}
print(dicionario)

# adicionar um elemento (chave/valor)

dicionario ["senha"] = "1234567"
print(dicionario)

# alterar um valor de uma chave especifica

dicionario.update({"telefone" : "(25) 8 8888-8888"})
print(dicionario)

